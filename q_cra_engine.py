# CRA MAPPING (language-independent logic)
# ============================================================================
def map_scan_to_cra(scan_data: Dict[str, Any], lang: str = "en") -> List[Dict[str, Any]]:
    """Map web scanner results to CRA articles."""
    results = []

    cipher = scan_data.get("cipher", {})
    certificate = scan_data.get("certificate", {})
    quantum_risks = scan_data.get("quantum_risks", [])
    headers = scan_data.get("http_headers", {})
    protocols = scan_data.get("supported_protocols", [])
    tls_version = scan_data.get("tls_version", "Unknown")

    def find_risk(keyword):
        for r in quantum_risks:
            if keyword.lower() in r.get("component", "").lower():
                return r
        return None

    # 1. Key Exchange
    kex_risk = find_risk("key exchange")
    kex_level = kex_risk.get("level", "UNKNOWN") if kex_risk else "UNKNOWN"
    kex_status = "PASS" if kex_level == "SAFE" else "WARNING" if kex_level in ("LOW", "MEDIUM") else "FAIL"
    results.append({
        "article": "Art.10(1)",
        "title": "Key Exchange Security",
        "category": "Product Security",
        "severity": "CRITICAL",
        "check": f"Key Exchange: {cipher.get('key_exchange', 'N/A')}",
        "status": kex_status,
        "detail": kex_risk.get("detail", "") if kex_risk else "",
        "remediation": kex_risk.get("recommendation", "") if kex_risk else "",
        "nis2": "Art.21(2)(e)",
    })

    # 2. PQC Readiness
    is_pqc = cipher.get("is_pqc", False)
    results.append({
        "article": "Art.10(5)",
        "title": "Post-Quantum Cryptography Readiness",
        "category": "Data Protection",
        "severity": "CRITICAL",
        "check": f"PQC Key Exchange: {'Detected' if is_pqc else 'Not Detected'}",
        "status": "PASS" if is_pqc else "FAIL",
        "detail": t("pqc_detected", lang) if is_pqc else t("pqc_not_detected", lang),
        "remediation": t("no_action", lang) if is_pqc else t("pqc_deploy", lang),
        "nis2": "Art.21(2)(e), Art.21(2)(h)",
    })

    # 3. TLS Version
    tls_status = "PASS" if "1.3" in tls_version else "WARNING" if "1.2" in tls_version else "FAIL"
    results.append({
        "article": "Art.10(1)",
        "title": "TLS Protocol Version",
        "category": "Product Security",
        "severity": "HIGH",
        "check": f"TLS: {tls_version}",
        "status": tls_status,
        "detail": f"{t('server_negotiated', lang)} {tls_version}",
        "remediation": t("no_action", lang) if tls_status == "PASS" else "Upgrade to TLS 1.3",
        "nis2": "Art.21(2)(e)",
    })

    # 4. Cipher Strength
    enc = cipher.get("encryption", "")
    enc_bits = cipher.get("encryption_bits", 0)
    enc_status = "PASS" if enc_bits >= 256 else "WARNING" if enc_bits >= 128 else "FAIL"
    results.append({
        "article": "Art.10(1)",
        "title": "Cipher Strength",
        "category": "Product Security",
        "severity": "HIGH",
        "check": f"{t('cipher_suite', lang)}: {enc} ({enc_bits}-bit)",
        "status": enc_status,
        "detail": f"{t('cipher_suite', lang)}: {cipher.get('name', 'N/A')}",
        "remediation": t("no_action", lang) if enc_status == "PASS" else "AES-256-GCM",
        "nis2": "Art.21(2)(e)",
    })

    # 5. Certificate
    cert_expired = certificate.get("is_expired", False)
    days_left = certificate.get("days_until_expiry", -1)
    cert_status = "FAIL" if cert_expired else "WARNING" if days_left < 30 else "PASS"
    results.append({
        "article": "Art.10(3)",
        "title": "Certificate Validity",
        "category": "Update Management",
        "severity": "HIGH",
        "check": f"{certificate.get('not_after', 'N/A')} ({days_left} {t('expires_days', lang)})",
        "status": cert_status,
        "detail": f"Subject: {certificate.get('subject', 'N/A')}, Issuer: {certificate.get('issuer', 'N/A')}",
        "remediation": t("no_action", lang) if cert_status == "PASS" else "Renew certificate",
        "nis2": "Art.21(2)(e)",
    })

    # 6. Certificate Key Quantum Risk
    cert_risk = find_risk("certificate key")
    cert_q_level = cert_risk.get("level", "UNKNOWN") if cert_risk else "UNKNOWN"
    cert_q_status = "PASS" if cert_q_level == "SAFE" else "WARNING" if cert_q_level in ("LOW", "MEDIUM") else "FAIL"
    results.append({
        "article": "Annex I.1",
        "title": "Certificate Key — Quantum Resistance",
        "category": "Security Design",
        "severity": "HIGH",
        "check": f"Key: {certificate.get('key_type', 'N/A')} ({certificate.get('key_size', 0)}-bit)",
        "status": cert_q_status,
        "detail": cert_risk.get("detail", "") if cert_risk else "Unknown",
        "remediation": cert_risk.get("recommendation", "") if cert_risk else "",
        "nis2": "Art.21(2)(e)",
    })

    # 7. Signature Algorithm
    sig_risk = find_risk("signature")
    sig_level = sig_risk.get("level", "UNKNOWN") if sig_risk else "UNKNOWN"
    sig_status = "PASS" if sig_level == "SAFE" else "WARNING" if sig_level in ("LOW", "MEDIUM") else "FAIL"
    results.append({
        "article": "Annex I.2",
        "title": "Signature Algorithm — Quantum Resistance",
        "category": "Vulnerability Handling",
        "severity": "MEDIUM",
        "check": f"Signature: {certificate.get('signature_algorithm', 'N/A')}",
        "status": sig_status,
        "detail": sig_risk.get("detail", "") if sig_risk else "",
        "remediation": sig_risk.get("recommendation", "") if sig_risk else "",
        "nis2": "Art.21(2)(e)",
    })

    # 8. Security Headers
    if headers:
        h_score = headers.get("score", 0)
        h_status = "PASS" if h_score >= 80 else "WARNING" if h_score >= 50 else "FAIL"
        missing = headers.get("missing_headers", [])
        results.append({
            "article": "Art.10(4)",
            "title": "HTTP Security Headers",
            "category": "Secure Defaults",
            "severity": "HIGH",
            "check": f"Header Score: {h_score}/100",
            "status": h_status,
            "detail": f"{t('missing_label', lang)}: {', '.join(missing)}" if missing else t("all_headers_ok", lang),
            "remediation": f"{t('add_label', lang)}: {', '.join(missing)}" if missing else t("no_action", lang),
            "nis2": "Art.21(2)(d)",
        })

        # 9. HSTS
        hsts = headers.get("hsts")
        results.append({
            "article": "Art.10(6)",
            "title": "HSTS — Transport Security Enforcement",
            "category": "Attack Surface",
            "severity": "HIGH",
            "check": f"HSTS: {t('present', lang) if hsts else t('missing_label', lang)}",
            "status": "PASS" if hsts else "FAIL",
            "detail": f"HSTS: {hsts}" if hsts else t("hsts_missing", lang),
            "remediation": t("no_action", lang) if hsts else t("hsts_add", lang),
            "nis2": "Art.21(2)(e)",
        })

    # 10. Deprecated protocols
    deprecated_found = []
    for p in protocols:
        if p.get("supported") and p.get("status") == "CRITICAL":
            deprecated_found.append(p.get("protocol", ""))
    if protocols:
        results.append({
            "article": "Art.10(6)",
            "title": "Deprecated Protocol Support",
            "category": "Attack Surface",
            "severity": "CRITICAL",
            "check": f"Deprecated: {', '.join(deprecated_found) if deprecated_found else t('deprecated_none', lang)}",
            "status": "FAIL" if deprecated_found else "PASS",
            "detail": f"Server: {', '.join(deprecated_found)}" if deprecated_found else t("no_deprecated", lang),
            "remediation": f"Disable: {', '.join(deprecated_found)}" if deprecated_found else t("no_action", lang),
            "nis2": "Art.21(2)(d)",
        })

    # Translate all results
    results = [translate_result(r, lang) for r in results]
    return results


# ============================================================================
# PDF GENERATOR (Multilingual)
# ============================================================================
class CRAReportPDF:
    """Generate CRA compliance PDF with language support."""

    def __init__(self, lang: str = "en"):
        self.lang = lang
        self.styles = getSampleStyleSheet()
        self._add_styles()

    def _add_styles(self):
        self.styles.add(ParagraphStyle("QTitle", parent=self.styles["Title"],
            fontSize=24, leading=30, textColor=C_PRIMARY, spaceAfter=4*mm))
        self.styles.add(ParagraphStyle("QH1", parent=self.styles["Heading1"],
            fontSize=16, leading=20, textColor=C_PRIMARY, spaceBefore=8*mm, spaceAfter=3*mm))
        self.styles.add(ParagraphStyle("QH2", parent=self.styles["Heading2"],
            fontSize=12, leading=16, textColor=C_PRIMARY, spaceBefore=5*mm, spaceAfter=2*mm))
        self.styles.add(ParagraphStyle("QBody", parent=self.styles["Normal"],
            fontSize=9, leading=13, textColor=C_TEXT, alignment=TA_JUSTIFY, spaceAfter=2*mm))
        self.styles.add(ParagraphStyle("QSmall", parent=self.styles["Normal"],
            fontSize=7, leading=9, textColor=C_DIM))

    def _header_footer(self, c, doc):
        c.saveState()
        w, h = A4
        c.setStrokeColor(C_ACCENT); c.setLineWidth(2)
        c.line(20*mm, h-18*mm, w-20*mm, h-18*mm)
        c.setFont("Helvetica-Bold", 7); c.setFillColor(C_PRIMARY)
        c.drawString(20*mm, h-15*mm, f"{COMPANY} | Q-CRA Dashboard v{VERSION}")
        c.setFont("Helvetica", 7); c.setFillColor(C_DIM)
        c.drawRightString(w-20*mm, h-15*mm, t("header_report", self.lang))
        c.setStrokeColor(C_NEUTRAL); c.setLineWidth(0.5)
        c.line(20*mm, 15*mm, w-20*mm, 15*mm)
        c.drawString(20*mm, 10*mm, f"{WEBSITE} | {datetime.now(timezone.utc).strftime('%Y-%m-%d %H:%M UTC')}")
        c.drawRightString(w-20*mm, 10*mm, f"Page {doc.page}")
        c.restoreState()

    def _sc(self, status):
        return {"PASS": C_SUCCESS, "WARNING": C_WARNING, "FAIL": C_DANGER}.get(status, C_NEUTRAL)

    def generate(self, scan_data: Dict, cra_results: List[Dict], output_path: str) -> str:
        lang = self.lang
        doc = SimpleDocTemplate(output_path, pagesize=A4,
            leftMargin=20*mm, rightMargin=20*mm, topMargin=22*mm, bottomMargin=20*mm,
            title=f"CRA Report — {scan_data.get('hostname', 'Unknown')}",
            author=COMPANY)

        el = []

        # Cover
        el.append(Spacer(1, 25*mm))
        el.append(Paragraph(t("report_title", lang).replace("\n", "<br/>"), self.styles["QTitle"]))
        el.append(Paragraph(f"{t('generated_by', lang)} v{VERSION} | {COMPANY}", self.styles["QSmall"]))
        el.append(Spacer(1, 10*mm))

        total = len(cra_results)
        passes = sum(1 for r in cra_results if r["status"] == "PASS")
        warnings = sum(1 for r in cra_results if r["status"] == "WARNING")
        fails = sum(1 for r in cra_results if r["status"] == "FAIL")
        score = round((passes / total) * 100) if total else 0
        pqc = scan_data.get("cipher", {}).get("is_pqc", False)

        info = [
            [f"{t('target', lang)}:", scan_data.get("hostname", "N/A")],
            [f"{t('ip', lang)}:", scan_data.get("ip_address", "N/A")],
            [f"{t('tls', lang)}:", scan_data.get("tls_version", "N/A")],
            [f"{t('scan_time', lang)}:", scan_data.get("scan_timestamp", "N/A")],
            [f"{t('report_time', lang)}:", datetime.now(timezone.utc).strftime("%Y-%m-%d %H:%M UTC")],
            ["", ""],
            [f"{t('overall_score', lang)}:", f"{score}%"],
            [f"{t('checks', lang)}:", str(total)],
            [t("pass", lang) + ":", str(passes)],
            [t("warning", lang) + ":", str(warnings)],
            [t("fail", lang) + ":", str(fails)],
            [f"{t('pqc_ready', lang)}:", t("yes", lang) if pqc else t("no", lang)],
        ]
        tb = Table(info, colWidths=[40*mm, 110*mm])
        tb.setStyle(TableStyle([
            ("FONTNAME", (0,0), (0,-1), "Helvetica-Bold"),
            ("FONTSIZE", (0,0), (-1,-1), 9),
            ("TEXTCOLOR", (0,0), (0,-1), C_PRIMARY),
            ("BOTTOMPADDING", (0,0), (-1,-1), 3),
            ("LINEBELOW", (0,5), (-1,5), 0.5, C_NEUTRAL),
            ("BACKGROUND", (0,6), (-1,6), C_BG),
        ]))
        el.append(tb)
        el.append(Spacer(1, 8*mm))
        el.append(Paragraph(f"<b>Disclaimer:</b> {t('disclaimer', lang)}", self.styles["QSmall"]))
        el.append(PageBreak())

        # Executive Summary
        el.append(Paragraph(t("executive_summary", lang), self.styles["QH1"]))
        hdr = [t("cra_article", lang), t("check", lang), t("category", lang), t("status", lang)]
        td = [hdr]
        for r in cra_results:
            td.append([r["article"], r["title"], r["category"], r.get("status_display", r["status"])])
        et = Table(td, colWidths=[25*mm, 55*mm, 40*mm, 25*mm])
        scmds = [
            ("BACKGROUND", (0,0), (-1,0), C_PRIMARY), ("TEXTCOLOR", (0,0), (-1,0), white),
            ("FONTNAME", (0,0), (-1,0), "Helvetica-Bold"), ("FONTSIZE", (0,0), (-1,-1), 8),
            ("ALIGN", (3,0), (3,-1), "CENTER"), ("GRID", (0,0), (-1,-1), 0.4, C_NEUTRAL),
            ("ROWBACKGROUNDS", (0,1), (-1,-1), [white, C_BG]),
            ("BOTTOMPADDING", (0,0), (-1,-1), 4), ("TOPPADDING", (0,0), (-1,-1), 4),
        ]
        for i, r in enumerate(cra_results, 1):
            cl = self._sc(r["status"])
            scmds.append(("TEXTCOLOR", (3,i), (3,i), cl))
            scmds.append(("FONTNAME", (3,i), (3,i), "Helvetica-Bold"))
        et.setStyle(TableStyle(scmds))
        el.append(et)
        el.append(PageBreak())

        # Detailed Findings
        el.append(Paragraph(t("detailed_findings", lang), self.styles["QH1"]))
        for r in cra_results:
            cl = self._sc(r["status"])
            bl = []
            bl.append(Paragraph(
                f'<font color="{cl.hexval()}">[{r.get("status_display", r["status"])}]</font> '
                f'{r["article"]} — {r["title"]}',
                ParagraphStyle("FT", parent=self.styles["Normal"],
                    fontSize=10, fontName="Helvetica-Bold", textColor=C_PRIMARY, spaceAfter=1*mm)))
            bl.append(Paragraph(f'<b>{t("check", lang)}:</b> {r["check"]}', self.styles["QBody"]))
            if r.get("detail"):
                bl.append(Paragraph(f'<i>{r["detail"]}</i>', self.styles["QBody"]))
            if r.get("remediation") and r["remediation"] != t("no_action", lang):
                bl.append(Paragraph(f'<b>{t("remediation", lang)}:</b> {r["remediation"]}', self.styles["QBody"]))
            bl.append(Paragraph(f'<b>CRA:</b> {r["article"]} | <b>NIS2:</b> {r.get("nis2", "N/A")}', self.styles["QBody"]))
            bl.append(Spacer(1, 4*mm))
            el.append(KeepTogether(bl))

        # Recommendations
        el.append(PageBreak())
        el.append(Paragraph(t("recommendations", lang), self.styles["QH1"]))
        fails_list = [r for r in cra_results if r["status"] == "FAIL"]
        warns_list = [r for r in cra_results if r["status"] == "WARNING"]
        if fails_list:
            el.append(Paragraph(t("priority1", lang), self.styles["QH2"]))
            for r in fails_list:
                el.append(Paragraph(f'<b>{r["article"]}</b> — {r["title"]}: {r.get("remediation", "")}', self.styles["QBody"]))
        if warns_list:
            el.append(Paragraph(t("priority2", lang), self.styles["QH2"]))
            for r in warns_list:
                el.append(Paragraph(f'<b>{r["article"]}</b> — {r["title"]}: {r.get("remediation", "")}', self.styles["QBody"]))
        if not pqc:
            el.append(Paragraph(t("pqc_migration", lang), self.styles["QH2"]))
            el.append(Paragraph(t("pqc_migration_text", lang), self.styles["QBody"]))

        el.append(Spacer(1, 15*mm))
        el.append(Paragraph(f"{t('report_footer', lang)} v{VERSION} | {COMPANY} | {WEBSITE}", self.styles["QSmall"]))

        doc.build(el, onFirstPage=self._header_footer, onLaterPages=self._header_footer)
        return output_path
