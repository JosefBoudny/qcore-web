TLD_LANG_MAP = {
    ".cz": "cs", ".sk": "cs",
    ".de": "de", ".at": "de", ".ch": "de",
}

def detect_language(hostname: str) -> str:
    """Detect language from domain TLD."""
    hostname = hostname.lower().rstrip(".")
    for tld, lang in TLD_LANG_MAP.items():
        if hostname.endswith(tld):
            return lang
    return "en"

